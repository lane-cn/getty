package cn.batchfile.getty.application;

public class ApplicationListener {

	private String start;
	private String stop;
	
	public String getStart() {
		return start;
	}
	
	public void setStart(String start) {
		this.start = start;
	}
	
	public String getStop() {
		return stop;
	}
	
	public void setStop(String stop) {
		this.stop = stop;
	}
}
